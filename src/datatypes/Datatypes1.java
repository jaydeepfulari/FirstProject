package datatypes;

public class Datatypes1 
{
	public static void main(String[] args) 
{
		Boolean a = true;
		System.out.println(a);
		
		byte b = 127;
		System.out.println(b);
		
		short c = 32767;
		System.out.println(c);
		
		int d = 2147483647;
		System.out.println(d);
				
		long e = 99999999999999L;
		System.out.println(e);
		
		float f = 99999999999999999999999999999999999999.1111111111111111111f;
		System.out.println(f);
		
		double g = 99999999999999999999999999999999999999999999999999999.1111111111111111111d;
		System.out.println(g);
		
		char h = 'A';
		System.out.println(h);
		
		
}

}
