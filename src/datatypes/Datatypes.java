package datatypes;

public class Datatypes
{
public static void main (String[] args)
{
	boolean a = true;
	System.out.println(a);
	
	byte b = 127;
	System.out.println(b);
	
	short s = 32767;
	System.out.println(s);
	
	int i = 2147483647;
	System.out.println(i);
	
	long l = 2^63;
	System.out.println(l);
			
	float f = 99999999999999999999999999999999999999.1111111111111111111f;
	System.out.println(f);
			
	double d= 99999999999999999999999999999999999999999999999999999.1111111111111111111d;
	System.out.println(d);
}
}
